package Day0324.실습시험김건희;

public class 문제5번 {
	public static void Hello() {
        char[] ch = {'h', 'e', 'l', 'l', 'o', ' ', 'j', 'a', 'v', 'a'};
        
        for (int i = 0; i < ch.length; i++) {
            System.out.print(ch[i]);
        }
    }

    public static void main(String[] args) {
        Hello();
    }
}

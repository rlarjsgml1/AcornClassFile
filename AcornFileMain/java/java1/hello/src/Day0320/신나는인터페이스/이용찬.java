package Day0320.신나는인터페이스;

public class 이용찬 implements 많이먹는게가능한, 변신가능한, 싸움이가능한17대1, 태권도가가능한 {

	@Override
	public void 변신하기() {
		
		System.out.println("집에 가면 아들로 변신해요~~");
		
	}

	@Override
	public void 많이먹다() {
		
		System.out.println("나는 햄버거를 5개 먹을 수 있어요");
	}

	@Override
	public void 태권도하기() {
		
		System.out.println("발차기를 하다가 넘어졌습니다!");
		
	}

	@Override
	public void 싸움하기() {

		System.out.println("줄행랑");
		
	}

	
	
}

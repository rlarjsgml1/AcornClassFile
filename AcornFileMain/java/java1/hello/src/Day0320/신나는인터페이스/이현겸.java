package Day0320.신나는인터페이스;

public class 이현겸 implements 많이먹는게가능한, 나는게가능한, 태권도가가능한, 불어가능한 {

	@Override
	public void 날수있다() {
		
		System.out.println("이현겸은 날아서 집까지 갔다");
		
	}
	
	@Override
	public void 많이먹다() {
		
		System.out.println("이현겸은 봄동비빔밥에 밥을 두 공기 넣어 먹었다!");
	}
	
	@Override
	public void 태권도하기() {
		
		System.out.println("이현겸은 격파를 했다 아이야~");
		
	}
	
	@Override
	public void 불어를한다() {
	
		System.out.println("봉수와");
		
	}
	
}

package Day0320.라이브러리;

class MyUtil_김민경 {

	
	
	
	public void 별출력하기() {
		
		//1.   별을 3개 출력하기         :   무조건 별을 3개 출력해 드릴께요
		
		for(int i=0; i<3; i++) {
			System.out.print("*");
		}
		System.out.println();
	}	
	
	//2.  원하는 문자를 3개 출력하기 ( 매개변수 활용)  :   당신이 원하는 문자를 3번 출력해 드릴께요
		
	public void print(String a) {
		for(int i=0; i<3; i++) {
			System.out.print(a);
		}
	}
	
	//3.  원하는 코드를 3번 실행하기  ( 매개변수 활용) :   당신의 코드를 3번 실행해 드릴께요
	
	
	
}
